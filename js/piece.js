// 미노 섞기 - 7-bag
function makeBag() {
  const types = Object.keys(MINOS_ROT);
  
  for (let i = types.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1)); // 무작위 선택
    [types[i], types[j]] = [types[j], types[i]];
  }

  return types.slice();
}

